export function getCodeFromLanguage(languageName) {
    return languageNameToCode[languageName] || null;
}

export function getLanguages(){
    return Object.entries(languageNameToCode)
        .filter(([name, code]) => code && code.trim() !== "")
        .map(([name]) => name)
        .sort();
}

export function getLanguageFromCode(code) {
    for (const [name, langCode] of Object.entries(languageNameToCode)) {
        if (langCode === code) {
            return name;
        }
    }
    return null;
}